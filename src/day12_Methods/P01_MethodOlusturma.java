package day12_Methods;

public class P01_MethodOlusturma {
    public static void main(String[] args) {

        String STR = "Java güzeldir"; // BU BİR METHOD'DUR (BÜYÜK YAZDIRIR)
        System.out.println(STR.toUpperCase()); // JAVA GİDER BUNU ÇAĞIRIR BUDA İŞİNİ GEREKTİĞİ ZAMAN YAPAR

        System.out.println(STR.toLowerCase()); // java yine gider çağırır ama bu sefer başka bir iş verir (küçük yazdırır)
    }

}

